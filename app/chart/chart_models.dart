class CandleData {
  final int time;
  final double open, high, low, close, volume;
  const CandleData({
    required this.time,
    required this.open,
    required this.high,
    required this.low,
    required this.close,
    required this.volume,
  });
  factory CandleData.fromBinanceKline(List<dynamic> k) => CandleData(
    time: (k[0] as int) ~/ 1000,
    open: double.parse(k[1].toString()),
    high: double.parse(k[2].toString()),
    low: double.parse(k[3].toString()),
    close: double.parse(k[4].toString()),
    volume: double.parse(k[5].toString()),
  );
  factory CandleData.fromBinanceWsKline(Map<String, dynamic> k) => CandleData(
    time: (k['t'] as int) ~/ 1000,
    open: double.parse(k['o'].toString()),
    high: double.parse(k['h'].toString()),
    low: double.parse(k['l'].toString()),
    close: double.parse(k['c'].toString()),
    volume: double.parse(k['v'].toString()),
  );
  Map<String, dynamic> toJson() => {
    'time': time,
    'open': open,
    'high': high,
    'low': low,
    'close': close,
    'volume': volume,
  };
}

class Ticker24h {
  final double high, low, vol, turnover, changePct, last;
  const Ticker24h({
    required this.high,
    required this.low,
    required this.vol,
    required this.turnover,
    required this.changePct,
    required this.last,
  });
  factory Ticker24h.fromBinance(Map<String, dynamic> j) => Ticker24h(
    high: double.parse(j['highPrice'].toString()),
    low: double.parse(j['lowPrice'].toString()),
    vol: double.parse(j['volume'].toString()),
    turnover: double.parse(j['quoteVolume'].toString()),
    changePct: double.parse(j['priceChangePercent'].toString()),
    last: double.parse(j['lastPrice'].toString()),
  );
  factory Ticker24h.fallback() => const Ticker24h(
    high: 74053.28,
    low: 68037.76,
    vol: 6540,
    turnover: 469170000,
    changePct: 5.77,
    last: 72045.85,
  );
}

/// One side of the order book (bids or asks)
class OrderBookLevel {
  final double price;
  final double qty;
  const OrderBookLevel(this.price, this.qty);
}

class OrderBook {
  final List<OrderBookLevel> bids; // sorted best→worst (highest first)
  final List<OrderBookLevel> asks; // sorted best→worst (lowest first)
  const OrderBook({required this.bids, required this.asks});
  factory OrderBook.empty() => const OrderBook(bids: [], asks: []);
}

